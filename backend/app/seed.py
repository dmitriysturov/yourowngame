def run_seed() -> None:
    with SessionLocal() as db:
        exists = db.scalar(select(GameTemplate).where(GameTemplate.title == "Демо: Своя игра"))
        if exists:
            return

        template = GameTemplate(title="Демо: Своя игра", description="Тестовый набор 5x5 для локальной разработки")
        db.add(template)
        db.flush()

        for cat_order, (category_title, questions) in enumerate(DEMO_CATEGORIES.items()):
            category = Category(template_id=template.id, title=category_title, order=cat_order)
            db.add(category)
            db.flush()
            for q_order, (q_text, q_answer, q_value) in enumerate(questions):
                db.add(
                    Question(
                        category_id=category.id,
                        text=q_text,
                        answer=q_answer,
                        value=q_value,
                        order=q_order,
                        has_media=False,
                    )
                )

        db.commit()


if __name__ == "__main__":
    run_seed()
